// 픽셀 배열 기반 캡챠 예측
async function solveCaptcha(imageData) {
    const image = await jimp.Jimp.fromBitmap(imageData);
    // 캡챠 글자는 7px, 4px 여백이 있음
    image.crop({ x: 7, y: 4, w: image.width - 7, h: image.height - 4 });

    const data = image.bitmap.data;
    for (let i = 0; i < data.length; i += 4) {
        if (data[i+3] === 255) data[i+3] = 1; // 글자를 가리는 가로선 제거
    }

    const predicted = [];
    for (let _ = 0; _ < 4; _++) {
        let numberMatches = [];
        for (const number of numbers) {
            const clone = image.clone().crop({ x: 0, y: 0, w: number[0].length, h: number.length });
            const cloneData = clone.bitmap.data;
            const numberData = number.flat();

            let matches = 0;
            for (let i = 0; i < cloneData.length; i += 4) {
                const alpha = cloneData[i+3];
                if (alpha === 1) matches++; // 가로선 부분은 무시
                else if (alpha === numberData[i/4]) matches++;
            }
            numberMatches.push(matches / cloneData.length);
        }

        let alphabetMatches = [];
        for (const alphabet of alphabets) {
            const clone = image.clone().crop({ x: 0, y: 0, w: alphabet[0].length, h: alphabet.length });
            const cloneData = clone.bitmap.data;
            const alphabetData = alphabet.flat();

            let matches = 0;
            for (let i = 0; i < cloneData.length; i += 4) {
                const alpha = cloneData[i+3];
                if (alpha === 1) matches++; // 가로선 부분은 무시
                else if (alpha === alphabetData[i/4]) matches++;
            }
            alphabetMatches.push(matches / cloneData.length);
        }

        const numberMaxMatched = Math.max(...numberMatches);
        const alphabetMaxMatched = Math.max(...alphabetMatches);
        let box;
        if (numberMaxMatched > alphabetMaxMatched) {
            const predictedNumber = numberMatches.indexOf(numberMaxMatched);
            predicted.push(predictedNumber);
            box = numberBoxes[predictedNumber];
        } else {
            const predictedAlphabet = alphabetMatches.indexOf(alphabetMaxMatched);
            predicted.push(String.fromCharCode(65 + predictedAlphabet));
            box = alphabetBoxes[predictedAlphabet];
        }
        image.crop({ x: box.x, y: 0, w: image.width - box.x, h: image.height });
    }

    return predicted.join("");
}
